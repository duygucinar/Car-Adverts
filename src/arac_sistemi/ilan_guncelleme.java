    private void araba_idActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_araba_idActionPerformed
        baglanti bg = new baglanti();
        boolean kontrol=false,kontrol2=false;
        String a_id="";
        String ID = ilan_id.getText();
        
        
        try {
            
            bg.myRs = bg.myStat.executeQuery("select * from ilan");
            
            
            while(bg.myRs.next()) {
                if(bg.myRs.getString("ilan_ID").equalsIgnoreCase(ID)){
                    kontrol=true;
                    
                    a_id = JOptionPane.showInputDialog(null,"ARABA ID");
                
                    bg.myRs = bg.myStat.executeQuery("select * from araba");
                
                    while(bg.myRs.next()) {
                        if(bg.myRs.getString("araba_ID").equalsIgnoreCase(a_id)){
                            kontrol2=true;
                            bg.myStat.executeUpdate("update ilan set ilan_arabaid='"+a_id+"' where ilan_ID="+Integer.parseInt(ID));
                            JOptionPane.showMessageDialog(null,"'"+ID+"' ID numaralı ilanın Araba ID güncellendi");
                            break;
                        }
                    }
                    
                    if(!kontrol2){
                        JOptionPane.showMessageDialog(null,"'"+a_id+"' numaralı araba bulunmamaktadır"); 
                    }
                    
                    break;
                }
            }
            
            if(!kontrol){
               JOptionPane.showMessageDialog(null,"'"+ID+"' numaralı ilan bulunmamaktadır"); 
            }  
            
        } catch (SQLException ex) {
            Logger.getLogger(kayit_silme.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_araba_idActionPerformed

    private void sehir_idActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sehir_idActionPerformed
        baglanti bg = new baglanti();
        boolean kontrol=false,kontrol2=false;
        String s_id="";
        String ID = ilan_id.getText();
        
        
        try {
            
            bg.myRs = bg.myStat.executeQuery("select * from ilan");
            
            
            while(bg.myRs.next()) {
                if(bg.myRs.getString("ilan_ID").equalsIgnoreCase(ID)){
                    kontrol=true;
                    
                    s_id = JOptionPane.showInputDialog(null,"ŞEHİR ID");
                
                    bg.myRs = bg.myStat.executeQuery("select * from sehirler");
                
                    while(bg.myRs.next()) {
                        if(bg.myRs.getString("sehir_ID").equalsIgnoreCase(s_id)){
                            kontrol2=true;
                            bg.myStat.executeUpdate("update ilan set ilan_sehirid='"+s_id+"' where ilan_ID="+Integer.parseInt(ID));
                            JOptionPane.showMessageDialog(null,"'"+ID+"' ID numaralı ilanın Şehir ID güncellendi");
                            break;
                        }
                    }
                    
                    if(!kontrol2){
                        JOptionPane.showMessageDialog(null,"'"+s_id+"' plakalı şehir bulunmamaktadır"); 
                    }
                    
                    break;
                }
            }
            
            if(!kontrol){
               JOptionPane.showMessageDialog(null,"'"+ID+"' numaralı ilan bulunmamaktadır"); 
            }  
            bg.myRs.close();
            bg.myStat.close();
        } catch (SQLException ex) {
            Logger.getLogger(kayit_silme.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_sehir_idActionPerformed

