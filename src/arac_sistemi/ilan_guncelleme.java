    private void ilan_tarihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ilan_tarihActionPerformed
        baglanti bg = new baglanti();
        int kontrol=0;
        String ID = ilan_id.getText();
        
        
        try {
            
            bg.myRs = bg.myStat.executeQuery("select * from ilan");
            
            
            while(bg.myRs.next()) {
                if(bg.myRs.getString("ilan_ID").equalsIgnoreCase(ID)){
                    kontrol=1;
                    break;
                }
            }
            
            if(kontrol==1){
                String tarih = JOptionPane.showInputDialog(null,"İLAN KM");
                bg.myStat.executeUpdate("update ilan set ilan_tarih='"+tarih+"' where ilan_ID="+Integer.parseInt(ID));
                JOptionPane.showMessageDialog(null,"'"+ID+"' ID numaralı ilan tarihi güncellendi");
            }
            else
                JOptionPane.showMessageDialog(null,"'"+ID+"' numaralı ilan bulunmamaktadır");
            
        } catch (SQLException ex) {
            Logger.getLogger(kayit_silme.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ilan_tarihActionPerformed

