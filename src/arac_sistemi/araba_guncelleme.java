    private void vites_turuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vites_turuActionPerformed
        baglanti bg = new baglanti();
        int kontrol=0,kontrol2=0;
        String ID = araba_id.getText();
        String vites = "";
        
        
        try {
            
            bg.myRs = bg.myStat.executeQuery("select * from araba");
            
            
            while(bg.myRs.next()) {
                if(bg.myRs.getString("araba_ID").equalsIgnoreCase(ID)){
                    kontrol=1;
                    break;
                }
            }
            
            if(kontrol==1){
                vites = JOptionPane.showInputDialog(null,"ARABA VİTES TÜRÜ");
            
                bg.myRs = bg.myStat.executeQuery("select * from vites");
                while(bg.myRs.next()) {
                    if(bg.myRs.getString("vitesturu_ID").equalsIgnoreCase(vites)){
                        kontrol2=1;
                        break;
                    }
                }
                
                if(kontrol2==0){
                    JOptionPane.showMessageDialog(null,"'"+vites+"' ID numaralı vites türü bulunmamaktadır.");
                }
            }
            else
                JOptionPane.showMessageDialog(null,"'"+ID+"' numaralı araba bulunmamaktadır");
            
            if(kontrol==1 && kontrol2==1){
                
                bg.myStat.executeUpdate("update araba set araba_vitesturuid='"+vites+"' where araba_ID="+ID);
                JOptionPane.showMessageDialog(null,"'"+ID+"' ID numaralı Araba Vites Türü güncellendi");
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(kayit_silme.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_vites_turuActionPerformed

    private void yakit_turuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_yakit_turuActionPerformed
        baglanti bg = new baglanti();
        boolean kontrol=false,kontrol2=false;
        String ID = araba_id.getText();
        String yakit = "";
        
        try {
            bg.myRs = bg.myStat.executeQuery("select * from araba");
            
            while(bg.myRs.next()){
                if(bg.myRs.getString("araba_id").equalsIgnoreCase(ID)){
                    kontrol=true;
                    
                    bg.myRs = bg.myStat.executeQuery("select * from yakit");
                    yakit = JOptionPane.showInputDialog(null,"YAKIT TÜRÜ ID");
                    while(bg.myRs.next()){
                        if(bg.myRs.getString("yakitturu_id").equalsIgnoreCase(yakit)){
                            kontrol2=true;
                            bg.myStat.executeUpdate("update araba set araba_yakitturuid='"+yakit+"' where araba_ID="+ID);
                            JOptionPane.showMessageDialog(null,"'"+ID+"' ID numaralı Araba Yakıt Türü güncellendi");
                            break;
                        }
                    }
                    
                    if(!kontrol2){
                        JOptionPane.showMessageDialog(null,"'"+yakit+"' ID numaralı Yakıt Türü bulunmamaktadır");
                    }
                    break;
                }
            }
            
            if(!kontrol){
               JOptionPane.showMessageDialog(null,"'"+ID+"' ID numaralı Araba bulunmamaktadır"); 
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(araba_guncelleme.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_yakit_turuActionPerformed

