    private void araba_markaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_araba_markaActionPerformed
        baglanti bg = new baglanti();
        int kontrol=0;
        String ID = araba_id.getText();
        
        
        try {
            
            bg.myRs = bg.myStat.executeQuery("select * from araba");
            
            
            while(bg.myRs.next()) {
                if(bg.myRs.getString("araba_ID").equalsIgnoreCase(ID)){
                    kontrol=1;
                    break;
                }
            }
            
            if(kontrol==1){
                String marka = JOptionPane.showInputDialog(null,"ARABA MARKA");
                bg.myStat.executeUpdate("update araba set araba_marka='"+marka+"' where araba_ID="+ID);
                JOptionPane.showMessageDialog(null,"'"+ID+"' ID numaralı Araba Marka güncellendi");
            }
            else
                JOptionPane.showMessageDialog(null,"'"+ID+"' numaralı araba bulunmamaktadır");
            
        } catch (SQLException ex) {
            Logger.getLogger(kayit_silme.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_araba_markaActionPerformed

